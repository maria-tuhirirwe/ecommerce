export async function getProductById(id: string) {
  if (!supabase || !isSupabaseConfigured) {
    throw new Error('Supabase is not configured')
  }
  
  const { data, error } = await supabase
    .from('products')
    .select(`
      id, name, slug, description, price_cents, stock, images, active, category_id, created_at, updated_at,
      categories (
        name
      )
    `)
    .eq('id', parseInt(id))
    .single()
  
  if (error) {
    if (error.code === 'PGRST116') return null // Not found
    throw error
  }
  
  return {
    id: data.id.toString(),
    name: data.name,
    slug: data.slug,
    price_cents: data.price_cents,
    stock: data.stock,
    images: data.images,
    description: data.description,
    active: data.active,
    categoryId: data.category_id.toString(),
    categoryName: (data.categories as any)?.name || 'Unknown',
    created_at: data.created_at,
    updated_at: data.updated_at
  }
}

export async function getRelatedProducts(categoryId: string, excludeId?: string) {
  if (!supabase || !isSupabaseConfigured) {
    throw new Error('Supabase is not configured')
  }
  
  let query = supabase
    .from('products')
    .select(`
      id, name, slug, description, price_cents, stock, images, active, category_id, created_at, updated_at,
      categories (
        name
      )
    `)
    .eq('category_id', parseInt(categoryId))
    .order('created_at', { ascending: false })
    .limit(4)
  
  if (excludeId) {
    query = query.neq('id', parseInt(excludeId))
  }
  
  const { data, error } = await query
  
  if (error) throw error
  
  return (data || []).map(product => ({
    id: product.id.toString(),
    name: product.name,
    slug: product.slug,
    price_cents: product.price_cents,
    stock: product.stock,
    images: product.images,
    description: product.description,
    active: product.active,
    categoryId: product.category_id.toString(),
    categoryName: (product.categories as any)?.name || 'Unknown',
    created_at: product.created_at,
    updated_at: product.updated_at
  }))
}
